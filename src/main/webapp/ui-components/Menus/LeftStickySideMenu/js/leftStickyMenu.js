function addContent(data){
	var ContentHtmlString = '';
	var ListHtmlString = '';
	for(i = 0 ; i < data.length; i++){
		ListHtmlString += '<a href="#'+data[i].id+'" class="list-group-item">'+data[i].heading+'</a>';
		ContentHtmlString += '<div class="render-content">'+
							'<h4 class="render-title" id="'+data[i].id+'">'+data[i].heading+'</h4>'+
							'<p>'+data[i].content+'</p>';
	}
	$('#side-menu').append(ListHtmlString);
	$('.render-block').append(ContentHtmlString);

}
